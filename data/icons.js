function getIcon(name, size = 28, isBot = false) {
  const allIcons = Object.assign(
    {},
    BOT_ICONS,
    BUILDING_ICONS,
    METALLURGY_ICONS,
    HANDHELD_ICONS,
    BUILDINGS2_ICONS,
    MISC_ICONS,
    COMPONENTS_ICONS,
    DECOR_ICONS,
  );
  const resolve = (n) => allIcons[n];

  const b64 = resolve(name) || (ICON_ALIASES[name] ? resolve(ICON_ALIASES[name]) : null);
  if (b64)
    return `<img src="${b64}" style="width:${size}px;height:${size}px;flex-shrink:0;border-radius:3px;object-fit:cover;" title="${name}">`;
  return `<div style="width:${size}px;height:${size}px;background:rgba(255,255,255,0.08);border-radius:6px;border:1px solid rgba(255,255,255,0.12);flex-shrink:0" title="${name}"></div>`;
}

